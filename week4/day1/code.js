//=== git add, commit, push, pull
/*
## mengambil code dari repository remote
git pull origin <branch>

## menambahkan code untuk di commit
git add <file1> <file2> <etc>

## membuat commit dari file yang sudah di add
git commit -m "message to commit code"

## mengupload code ke repository remote
git push origin <name_branch>
*/

//=== git checkout, branch
/*
## membuat branch baru
git branch <name_branch>
git checkout -b <name_branch>

## pindah ke branch
git checkout <name_branch>

## menghapus branch
git branch -d <name_branch>
*/

//=== git merge
/*
## menggabungkan branch sekarang dengan branch tujuan
git merge <name_branch>
*/
